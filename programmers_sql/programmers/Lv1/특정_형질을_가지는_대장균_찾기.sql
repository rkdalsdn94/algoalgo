# 프로그래머스 SQL - Lv1 - 특정 형질을 가지는 대장균 찾기 - SELECT(비트마스킹) 문제
/**
    SELECT 문제 - 비트마스킹(Bit Masking)

    비트 연산자 &을 사용하여 특정 비트가 설정되어 있는지 확인하는 문제
    GENOTYPE 컬럼의 정수값을 이진수로 해석하여 각 비트가 형질을 나타낸다.
     - 1번 형질: 1번째 비트 (값: 1)
     - 2번 형질: 2번째 비트 (값: 2)
     - 3번 형질: 3번째 비트 (값: 4)

    조건
     - 1. 2번 형질이 없어야 함: (GENOTYPE & 2) = 0
     - 2. 1번이나 3번 형질을 보유해야 함: (GENOTYPE & 1) > 0 OR (GENOTYPE & 4) > 0

    비트 연산의 AND(&) 연산을 통해 특정 비트의 값을 확인할 수 있다.
    예: 13 & 4 = 1101₂ & 0100₂ = 0100₂ = 4 > 0 (3번 형질 보유)
 */

SELECT COUNT(*) AS COUNT
FROM ECOLI_DATA
WHERE (GENOTYPE & 2) = 0  -- 2번 형질이 없음
  AND ((GENOTYPE & 1) > 0 OR (GENOTYPE & 4) > 0);  -- 1번이나 3번 형질 보유
