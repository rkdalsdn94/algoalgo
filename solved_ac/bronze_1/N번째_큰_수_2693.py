'''
정렬 문제

단순히 정렬한 후 뒤에서 3번째 값을 출력하면 된다.

in
    4
    1 2 3 4 5 6 7 8 9 1000
    338 304 619 95 343 496 489 116 98 127
    931 240 986 894 826 640 965 833 136 138
    940 955 364 188 133 254 501 122 768 408
out
    8
    489
    931
    768
'''

