# 백준 - 브론즈1 - 소수가 아닌 수 3 - 수학, 애드 혹, 정수론 문제
"""
수학, 애드 혹, 정수론 문제

[핵심 아이디어]
    1. 0이 있으면 0 출력 (0은 소수가 아님)
    2. 1이 있으면 1 출력 (1은 소수가 아님)
    3. 짝수(4,6,8)가 있으면 그 수 출력 (모두 합성수)
    4. 2가 있으면 22 출력 (22 = 2 × 11)
    5. 홀수만 있는 경우: 1을 제외한 홀수로 같은 수 두 개 붙이기

[풀이 과정]
    1. 0이 있는지 확인 -> 있으면 0 출력
    2. 1이 있는지 확인 -> 있으면 1 출력 (1은 소수가 아님!)
    3. 4, 6, 8 중 하나라도 있으면 그 수 출력
    4. 2가 있으면 22 출력
    5. 나머지 홀수들(3,5,7,9)로 같은 수 두 개 붙이기
       - 주의: 11은 소수이므로 1로는 이 방법 사용 불가
"""

n = int(input())
n_list = list(map(int, input().split()))

# 테스트
# n = 4
# n_list = [2, 3, 6, 7]

# 1. 0이 있으면 0 출력
if 0 in n_list:
    print("YES")
    print(0)
    exit()

# 2. 1이 있으면 1 출력 (1은 소수가 아님!)
if 1 in n_list:
    print("YES")
    print(1)
    exit()

# 3. 짝수 4, 6, 8이 있으면 출력 (모두 합성수)
for d in [4, 6, 8]:
    if d in n_list:
        print("YES")
        print(d)
        exit()

# 4. 2가 있으면 22 출력 (22 = 2 × 11)
if 2 in n_list:
    print("YES")
    print(22)
    exit()

# 5. 나머지 홀수들(3,5,7,9)만 있는 경우
# 33 = 3×11, 55 = 5×11, 77 = 7×11, 99 = 9×11 (모두 합성수)
for d in n_list:
    print("YES")
    print(str(d) + str(d))
    exit()
