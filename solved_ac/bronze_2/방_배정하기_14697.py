# 백준 - 브론즈2 - 방 배정하기 - 14697 - 그리디, 완전 탐색 문제
'''
그리디, 완전 탐색 문제

[핵심 아이디어]
    1. 모든 경우의 수를 완전 탐색으로 확인하여 가능한 방 배정 여부를 결정합니다
    2. 세 종류의 방(a, b, c인실)을 각각 0개부터 최대로 사용 가능한 개수까지 조합을 확인합니다
    3. 방 배정 시 모든 침대가 정확히 채워지는 경우(빈 침대가 없는 경우)가 있으면 1, 없으면 0을 출력합니다

[풀이 과정]
    1. 세 종류의 방 정원(a, b, c)과 전체 학생 수(n)를 입력받습니다
    2. 삼중 반복문으로 모든 가능한 방 배정 조합을 탐색합니다:
       - a인실: 0개부터 최대 n/a개까지 사용
       - b인실: 0개부터 최대 n/b개까지 사용
       - c인실: 0개부터 최대 n/c개까지 사용
    3. 각 조합에서 총 침대 수(a×i + b×j + c×k)가 학생 수(n)와 정확히 일치하는지 확인합니다
    4. 조건을 만족하는 조합이 발견되면 1을 출력하고 프로그램을 종료합니다
    5. 모든 조합을 확인한 후에도 조건을 만족하는 경우가 없으면 0을 출력합니다
'''

a, b, c, n = map(int, input().split())

# 테스트
# a, b, c, n = 5, 9, 12, 113 # 1
# a, b, c, n = 3, 6, 9, 112 # 0

for i in range(n // a + 1):
    for j in range(n // b + 1):
        for k in range(n // c + 1):
            if a * i + b * j + c * k == n:
                print(1)
                exit(0)

print(0)
