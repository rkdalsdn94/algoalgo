# 프로그래머스 - Lv1 - 크기가 작은 부분문자열 - 슬라이딩 윈도우 문제
'''
슬라이딩 윈도우 문제

[핵심 아이디어]
    1. 고정된 크기의 윈도우(p의 길이만큼)를 문자열 t에 적용하여 순차적으로 이동시킨다.
    2. 각 윈도우 위치에서 부분 문자열이 나타내는 숫자를 p와 비교한다.
    3. 부분 문자열이 p보다 작거나 같은 경우를 카운트한다.

[풀이 과정]
    1. p의 길이를 구하여 윈도우 크기로 설정한다.
    2. 시작 인덱스(start)와 끝 인덱스(end)를 초기화한다.
    3. 문자열 p를 정수로 변환하여 비교 대상으로 준비한다.
    4. 윈도우를 t의 끝까지 이동시키며:
       a. 현재 윈도우의 부분 문자열을 추출한다.
       b. 추출한 부분 문자열을 정수로 변환하여 p와 비교한다.
       c. 부분 문자열이 p보다 작거나 같으면 카운트를 증가시킨다.
       d. 윈도우를 한 칸 오른쪽으로 이동시킨다(start와 end를 1씩 증가).
    5. 최종 카운트 값을 반환한다.
'''

def solution(t, p):
    answer = 0
    start, end = 0, len(p)
    p = int(p)

    while end <= len(t):
        temp = t[start:end]

        if int(temp) <= p:
            answer += 1

        start += 1
        end += 1

    return answer

print(solution("3141592", "271") == 2)
print(solution("500220839878", "7") == 8)
print(solution("10203", "15") == 3)
